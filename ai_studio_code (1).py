def parse_color(c):
    if isinstance(c, tuple): return c
    return ((c >> 16) & 255, (c >> 8) & 255, c & 255)

def get_color(r, g, b):
    r = max(0, min(255, int(r)))
    g = max(0, min(255, int(g)))
    b = max(0, min(255, int(b)))
    return (r, g, b)

