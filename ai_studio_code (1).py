def get_event():
    global EVT, event_queue
    while True:
        if not event_queue:
            event_queue = list(pygame.event.get())
        if event_queue:
            event = event_queue.pop(0)
            if event.type == pygame.QUIT:
                EVT[EVT_TYPE] = EVT_QUIT
